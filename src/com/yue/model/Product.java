package com.yue.model;

public class Product {
	private String proId;
	private String proName;
	private float proBuyPrice;
	private int proCount;
	private String proDesc;
	private float proSalePrice;
	private float proSellPrice;
	private String proPicPath;
	private String draId;
	private String proType;
	public String getProId() {
		return proId;
	}
	public void setProId(String proId) {
		this.proId = proId;
	}
	public String getProName() {
		return proName;
	}
	public void setProName(String proName) {
		this.proName = proName;
	}
	public float getProBuyPrice() {
		return proBuyPrice;
	}
	public void setProBuyPrice(float proBuyPrice) {
		this.proBuyPrice = proBuyPrice;
	}
	public int getProCount() {
		return proCount;
	}
	public void setProCount(int proCount) {
		this.proCount = proCount;
	}
	public String getProDesc() {
		return proDesc;
	}
	public void setProDesc(String proDesc) {
		this.proDesc = proDesc;
	}
	public float getProSalePrice() {
		return proSalePrice;
	}
	public void setProSalePrice(float proSalePrice) {
		this.proSalePrice = proSalePrice;
	}
	public float getProSellPrice() {
		return proSellPrice;
	}
	public void setProSellPrice(float proSellPrice) {
		this.proSellPrice = proSellPrice;
	}
	public String getProPicPath() {
		return proPicPath;
	}
	public void setProPicPath(String proPicPath) {
		this.proPicPath = proPicPath;
	}
	public String getDraId() {
		return draId;
	}
	public void setDraId(String draId) {
		this.draId = draId;
	}
	public String getProType() {
		return proType;
	}
	public void setProType(String proType) {
		this.proType = proType;
	}
	
}
