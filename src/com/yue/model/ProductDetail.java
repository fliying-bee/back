package com.yue.model;

public class ProductDetail {
	private String proDetailId;
	private int proDetailCount;
	private String proDetailType;
	private String proId;
	public String getProDetailId() {
		return proDetailId;
	}
	public void setProDetailId(String proDetailId) {
		this.proDetailId = proDetailId;
	}
	public int getProDetailCount() {
		return proDetailCount;
	}
	public void setProDetailCount(int proDetailCount) {
		this.proDetailCount = proDetailCount;
	}
	public String getProDetailType() {
		return proDetailType;
	}
	public void setProDetailType(String proDetailType) {
		this.proDetailType = proDetailType;
	}
	public String getProId() {
		return proId;
	}
	public void setProId(String proId) {
		this.proId = proId;
	}
	
}
