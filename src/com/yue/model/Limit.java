package com.yue.model;

public class Limit {
	private String limId;
	private String limName;
	private String limDesc;
	public String getLimId() {
		return limId;
	}
	public void setLimId(String limId) {
		this.limId = limId;
	}
	public String getLimName() {
		return limName;
	}
	public void setLimName(String limName) {
		this.limName = limName;
	}
	public String getLimDesc() {
		return limDesc;
	}
	public void setLimDesc(String limDesc) {
		this.limDesc = limDesc;
	}
	
}
